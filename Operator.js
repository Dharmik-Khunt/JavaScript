//Arithmetic Operator
let a=5;
let b=2;
console.log("a + b = ",a+b);
console.log("a - b = ",a-b);
console.log("a * b = ",a*b);
console.log("a / b = ",a/b);
console.log("a ** b = ",a**b); //exponential(a^b)

//Uniray Operator (++,--)

console.log("++a ",++a);
console.log("a-- ",a--);

//Assigment Operator (=, +=, -=, *=, **=)
console.log("a+= ",a+=4);  //a=a+4

